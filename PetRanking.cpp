_RANK::_RANK(const _PET& Pet, const int& Level)
{
	RankS_VIT = static_cast<double>(Pet.BaseVit + 4.5);										// S ±Þ vital
	RankS_STR = static_cast<double>(Pet.BaseStr + 4.5);										// S ±Þ str
	RankS_TGH = static_cast<double>(Pet.BaseTgh + 4.5);										// S ±Þ tgh
	RankS_DEX = static_cast<double>(Pet.BaseDex + 4.5);										// S ±Þ dex
	RankS_InitNum = static_cast<double>(Pet.InitNum);											// S ±Þ initnum
	RankS_TotalScore = static_cast<double>(Pet.BaseVit + Pet.BaseStr + Pet.BaseTgh + Pet.BaseDex);	// ÃÑÇÕ

	switch (Pet.Rank)
	{
	case 0:
		LevelUpRandomBonus = 4.75;
		break;
	case 1:
		LevelUpRandomBonus = 4.95;
		break;
	case 2:
		LevelUpRandomBonus = 5.15;
		break;
	case 3:
		LevelUpRandomBonus = 5.35;
		break;
	case 4:
		LevelUpRandomBonus = 5.55;
		break;
	case 5:
		LevelUpRandomBonus = 5.75;
		break;
	default:
		LevelUpRandomBonus = 4.75;
		break;
	}

	// S Grade State
	RankS_HP = static_cast<double>((RankS_VIT * 4 + RankS_STR + RankS_TGH + RankS_DEX) * RankS_InitNum / 100.0);
	RankS_ATK = static_cast<double>((RankS_VIT * 0.1 + RankS_STR + RankS_TGH * 0.1 + RankS_DEX * 0.05) * RankS_InitNum / 100.0);
	RankS_DEF = static_cast<double>((RankS_VIT * 0.1 + RankS_STR * 0.1 + RankS_TGH + RankS_DEX * 0.05) * RankS_InitNum / 100.0);
	RankS_SPD = static_cast<double>(RankS_DEX * RankS_InitNum / 100.0);

	// S Grade Growth rate
	RankS_Growth_HP = (RankS_VIT * 4 + RankS_STR + RankS_TGH + RankS_DEX) * LevelUpRandomBonus / 100.0;
	RankS_Growth_ATK = (RankS_VIT * 0.1 + RankS_STR + RankS_TGH * 0.1 + RankS_DEX * 0.05) * LevelUpRandomBonus / 100.0;
	RankS_Growth_DEF = (RankS_VIT * 0.1 + RankS_STR * 0.1 + RankS_TGH + RankS_DEX * 0.05) * LevelUpRandomBonus / 100.0;
	RankS_Growth_SPD = RankS_DEX * LevelUpRandomBonus / 100.0;
	RankS_Growth_TOTAL = RankS_Growth_ATK + RankS_Growth_DEF + RankS_Growth_SPD;


	// S Grade expected capacity value
	RankS_Expected_HP = static_cast<int>(RankS_HP + RankS_Growth_HP * (Level - 1) - 0.03);
	RankS_Expected_ATK = static_cast<int>(RankS_ATK + RankS_Growth_ATK * (Level - 1) - 0.03);
	RankS_Expected_DEF = static_cast<int>(RankS_DEF + RankS_Growth_DEF * (Level - 1) - 0.03);
	RankS_Expected_SPD = static_cast<int>(RankS_SPD + RankS_Growth_SPD * (Level - 1) - 0.03);
	RankS_Expected_TOTAL = RankS_Expected_HP + RankS_Expected_ATK + RankS_Expected_DEF + RankS_Expected_SPD;
}

_RANK _PET::GetRank(const int& Level)
{
	return _RANK(*this, Level);
}

