void PetRanking::MouseClick()
{
	if (mouse.onceState & MOUSE_LEFT_CRICK)
	{
		if (btn[BTN_CLOSE] == HitDispNo)
		{
			int i;
			extern unsigned int MenuToggleFlag;
			extern void DeathMenuAction();
			extern BOOL saveUserSetting();
			extern int play_se(int tone, int x, int y);
			MenuToggleFlag ^= JOY_CTRL_A;
			DeathMenuAction();
			saveUserSetting();
			play_se(203, 320, 240);
			SetCheck(0);
		}
		else if (btn[BTN_UP_LV] == HitDispNo)
		{
			if (Level < 199)
				Level++;
		}
		else if (btn[BTN_DOWN_LV] == HitDispNo)
		{
			if (Level > 0)
				Level--;
		}
		else if (btn[BTN_INPUT] == 1)
		{
			GetKeyInputFocus(&Input);
		}
		else if (btn[BTN_OK] == HitDispNo)
		{
			if (Input.cnt == 0) return;
			int Id = PetData::Instance().GetId(Input.buffer);
			if (Id == -1)
			{
				Current = nullptr;
				return;
			}
			Current = PetData::Instance().Get(Id);
			if (Current == nullptr) return;
			TopPet.clear();
			lssproto_PETRANK_send(sockfd, Id);
		}
	}
	std::fill(&btn[0], &btn[BTN_END], 0);
}

void PetRanking::SetCheck(const int& _Check)
{
	this->_Check = _Check;
	if (_Check == 0)
	{
		if (Action != nullptr)
		{
			DeathAction(Action);
			Action = nullptr;
		}
	}
}

void PetRanking::Recv(char* Str)
{
	std::vector<std::string> Chunks = PetData::Instance().Split(Str, "|");
	TopPet.clear();
	_TopPet r;
	
	for (size_t i = 0; i < Chunks.size(); i++)
	{
		std::vector<std::string> s = PetData::Instance().Split(makeStringFromEscaped(const_cast<char*>(Chunks.at(i).c_str())), "|");

		strcpy(r.PlayerName, s.at(0).c_str());
		r.Lv = std::stoi(s.at(1));
		r.Hp = std::stoi(s.at(2));
		r.Atk = std::stoi(s.at(3));
		r.Def = std::stoi(s.at(4));
		r.Spd = std::stoi(s.at(5));
		r.STotal = std::stoi(s.at(6));
		r.GrwHp = std::stod(s.at(7));
		r.GrwAtk = std::stod(s.at(8));
		r.GrwDef = std::stod(s.at(9));
		r.GrwSpd = std::stod(s.at(10));
		r.GTotal = std::stod(s.at(11));

		TopPet.push_back(r);
	}

}

