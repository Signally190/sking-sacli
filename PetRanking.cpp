std::vector<std::string> PetData::Split(std::string text, std::string delim)
{
	std::vector<std::string> _vTemp;
	size_t pos = 0;
	std::string token;
	while (1)
	{
		if ((pos = text.find(delim)) == std::string::npos)
		{
			if (text.size() > 0)
				_vTemp.push_back(text);

			break;
		}
		token = text.substr(0, pos);
		_vTemp.push_back(token);
		text.erase(0, pos + delim.length());
	}
	return _vTemp;
}

