//  return: 0 ... 处理中
//          !0 ... 处理完毕
int HelpProc(){
	static int btnId[16];
	static BYTE seg_history[16];
	static ACTION *ptActMenuWin = NULL;
	static int seg_index=0, page_index=0, desc_index=0, history_index=0;
	int x = 110, y = 50;
	static int btnState[4];
	static DWORD dwPressTime;
	int id = 0;
	int focusId = 0;
	int i, j;
	LPSEG seg;
	LPPAGE page;
	LPDESC desc;
	//初始化
	if( HelpProcNo == 0){
		for( i = 0; i < sizeof(btnId)/sizeof(int); i++)
			btnId[i] = -2;
		for( i = 0; i < sizeof(btnState)/sizeof(int); i++)
			btnState[i] = 0;
		btnState[3] = 0;
		for( i = 0; i < 3; i++)
			btnState[i] = -1;
		seg_index = page_index = desc_index = 0;

		history_index = 0;				//曾经浏览过的history
		ptActMenuWin = MakeWindowDisp( x, y, 420, 380, CG_FIELD_HELP_WND, -1);
		HelpProcNo++;
		dwPressTime = 0;
#ifdef _JOBDAILY
		char look[10];
		strcpy(look,"dyedye");
		lssproto_JOBDAILY_send(sockfd,look);
#endif
	}
	//初始化了
	if( ptActMenuWin != NULL){
		id = -1;
		focusId = -1;

		seg = &help_seg[seg_index];
		page = &help_page[page_index];
		desc = help_index[seg_index]+page->desc_index;
#ifdef _ANGEL_SUMMON
		if( jumpHelpPage != 0 || jumpHelpSeg != 0 ) {
			page_index = jumpHelpPage;
			seg_index = jumpHelpSeg;
			
			jumpHelpPage = 0;
			jumpHelpSeg = 0;
			
			seg = &help_seg[seg_index];
			page = &help_page[page_index];
			desc = help_index[seg_index]+page->desc_index;
			
			btnState[1] = -1;
			if( seg->page_index < page_index)				//是否可回"??"
				btnState[1] = 0;
			btnState[2] = -1;
			if( (seg->page_index+seg->page_num-1) > page_index)//是否可按"??"
				btnState[2] = 0;
		}
#endif

		//视窗已打开
		if( ptActMenuWin->hp >= 1){
			//滑鼠位置和按钮的判断
			id = selGraId( btnId, sizeof(btnId)/sizeof(int));
			if( id == -1)
				if( (id=selFontId( &btnId[4], sizeof(btnId)/sizeof(int)-4)) >= 0){
					id += 4;
				}
			focusId = focusGraId( btnId, sizeof(btnId)/sizeof(int));
		}
		//检查其它的视窗打开时，关闭
		if( CheckMenuFlag()){
			id = 100;
		}else
		//按ESC关闭
		if( (joy_trg[ 0 ] & JOY_ESC) && GetImeString() == NULL){
			id = 100;
			play_se( 203, 320, 240);	//视窗关闭声
		}else
		//按HELP关闭
		if( HelpProcNo == 1000){
			id = 100;
		}

		//按钮押下后的弹起状态，处理按按钮的event
		if( dwPressTime){
			if( TimeGetTime()>(dwPressTime+100)){
				dwPressTime = 0;
				//按"上一层"
				if( btnState[0] == 1){
					if( history_index > 0){								//按"上一层"是否合法
						history_index--;
						seg_index = seg_history[history_index];			//取出上一层的seg index
						seg = &help_seg[seg_index];						//指向新的seg
						page_index = seg->page_index;
						page = &help_page[page_index];					//指向新的seg的page
						desc_index = page->desc_index;
						desc = help_index[seg_index]+page->desc_index;	//指向新page的desc
					}
					btnState[0] = -1;
					if( history_index > 0)								//是否可按"上一层"
						btnState[0] = 0;
					btnState[1] = -1;									//不能按"??"
					btnState[2] = -1;
					if( (seg->page_index+seg->page_num-1) > page_index)	//是否可按"??"
						btnState[2] = 0;
				}
				//按"??"
				if( btnState[1] == 1){
					if( seg->page_index < page_index){			//按"??"是否合法
						page_index--;
						page = &help_page[page_index];			//指向??
						desc_index = page->desc_index;
						desc = help_index[seg_index]+desc_index;//指向新一页的desc开头
						btnState[2] = 0;						//可按"??"
					}
					btnState[1] = -1;
					if( seg->page_index < page_index)			//是否可按"??"
						btnState[1] = 0;
				}
				//按"??"
				if( btnState[2] == 1){
					if( (seg->page_index+seg->page_num-1) > page_index){//按"??"是否合法
						page_index++;
						page = &help_page[page_index];					//指向??
						desc_index = page->desc_index;
						desc = help_index[seg_index]+desc_index;		//指向新一页的desc开头
						btnState[1] = 0;								//可按"??"
					}
					btnState[2] = -1;
					if( (seg->page_index+seg->page_num-1) > page_index)	//是否可按"??"
						btnState[2] = 0;
				}
				//按"离开"
				if( btnState[3] == 1){
					play_se( 203, 320, 240);	//视窗关闭声
					id = 100;
				}
			}

		}else{
			if( 0 <= id && id <= 3){
				//按按钮的动作
				btnState[id] = 1;
				id = -1;
				dwPressTime = TimeGetTime();
				play_se( 217, 320, 240);	//CLICK声
			}else if( 4 <= id && id <= 15){
				//按文字按钮的动作
				id -= 4;

				for( i = 0; i < page->desc_num; i++){
					if( id == i){	//寻找按了那一行文字按钮
						//记录上一层的buffer超过
						if( history_index >= 16){
							history_index = 15;
							for( j = 1; j < 16; j++)
								seg_history[j-1] = seg_history[j];
						}
						btnState[0] = 0;	//可按"上一层"
						seg_history[history_index] = (BYTE)seg_index;	//记录上一层的seg_index
						history_index++;
						seg_index = desc[i].linkto_seg_no;				//连结的seg index
						seg = &help_seg[seg_index];						//指向新的seg
						page_index = seg->page_index;					//此一seg的起始page index
						page = &help_page[page_index];					//指向新的page
						desc_index = page->desc_index;					//此一page的起始desc index
						desc = help_index[seg_index]+page->desc_index;	//指向新的desc
						btnState[1] = -1;
						if( seg->page_index < page_index)				//是否可回"??"
							btnState[1] = 0;
						btnState[2] = -1;
						if( (seg->page_index+seg->page_num-1) > page_index)//是否可按"??"
							btnState[2] = 0;
						break;
					}
				}
				id = -1;
				play_se( 217, 320, 240);	//CLICK声
			}
		}

		//视窗关闭
		if( id >= 0){
			DeathAction( ptActMenuWin);
			ptActMenuWin = NULL;
			return 1;
		}
		if( ptActMenuWin->hp >= 1){
			if( 0<=focusId && focusId<=3){
				//提示说明
				ShowBottomLineString( FONT_PAL_WHITE, HelpMsg[focusId]);
			}
			//初始化
			for( i = 0; i < sizeof(btnId)/sizeof(int); i++)
				btnId[i] = -2;
			//秀视窗底图
			StockDispBuffer( ((WINDOW_DISP *)ptActMenuWin->pYobi)->mx, ((WINDOW_DISP *)ptActMenuWin->pYobi)->my, DISP_PRIO_MENU, CG_FIELD_HELP_WND, 1);
			//秀按钮的动作   JL800600
			if( btnState[0] >= 0)
				btnId[0] = StockDispBuffer(x + 121 + 32 + DISPLACEMENT_X / 2, y + 350  + DISPLACEMENT_Y / 2, DISP_PRIO_IME3, CG_FIELD_HELP_PRELAYER + btnState[0], 2);
			if( btnState[1] >= 0)
				btnId[1] = StockDispBuffer(x + 192 + 32 + DISPLACEMENT_X / 2, y + 350  + DISPLACEMENT_Y / 2, DISP_PRIO_IME3, CG_FIELD_HELP_PREPAGE + btnState[1], 2);
			if( btnState[2] >= 0)
				btnId[2] = StockDispBuffer(x + 262 + 32 + DISPLACEMENT_X / 2, y + 350  + DISPLACEMENT_Y / 2, DISP_PRIO_IME3, CG_FIELD_HELP_NEXTPAGE + btnState[2], 2);
			if( btnState[3] >= 0)
				btnId[3] = StockDispBuffer(x + 332 + 32 + DISPLACEMENT_X / 2, y + 350  + DISPLACEMENT_Y / 2, DISP_PRIO_IME3, CG_FIELD_HELP_EXIT + btnState[3], 2);
			//秀title
			StockFontBuffer( x+20 + DISPLACEMENT_X / 2, y+35 + DISPLACEMENT_Y / 2, FONT_PRIO_FRONT, FONT_PAL_GREEN, seg->tile, 0);
			int xx;
			//秀出此页所有的文字
			for( i = 0; i < page->desc_num; i++){
				xx = x + 34 + DISPLACEMENT_X / 2;
				if( desc->bmpno){		//此行有图示
					StockDispBuffer( xx+desc->indent*14+help_bmp[desc->bmpno].x, y+i*25+70+help_bmp[desc->bmpno].y + DISPLACEMENT_Y / 2, DISP_PRIO_IME3, +help_bmp[desc->bmpno].bmpno, 0);
					xx += 28;
				}
				xx += desc->indent*14;

				if( desc->linkto_seg_no){
					//此行可选择
					btnId[i+4] = StockFontBuffer( xx, y+i*25+70 + DISPLACEMENT_Y / 2, FONT_PRIO_FRONT, desc->color, desc->description, 2);
				}else
					if(strcmp(desc->description,"jobdaily") ==0){
#ifdef _JOBDAILY
						char tmbuf[48];
						sprintf(tmbuf,"Page：%02d/%02d",page_index-56,seg->page_index+seg->page_num-57);
						StockFontBuffer( x+25 + DISPLACEMENT_X / 2, y+340 + DISPLACEMENT_Y / 2, FONT_PRIO_FRONT, 1, tmbuf, 0);

						char buf[10];
						sprintf(buf,"%d",jobdaily[(page_index-57)*10+i-1].JobId);
						if(!atoi(buf))	buf[0] = '\0'; //不印0出来
						StockFontBuffer(xx     ,y+i*25+70 + DISPLACEMENT_Y / 2,FONT_PRIO_FRONT,FONT_PAL_WHITE,buf,0);
						StockFontBuffer(xx+30  ,y+i*25+70 + DISPLACEMENT_Y / 2,FONT_PRIO_FRONT,FONT_PAL_WHITE,jobdaily[(page_index-57)*10+i-1].explain,0);
						StockFontBuffer(xx+328 ,y+i*25+70 + DISPLACEMENT_Y / 2,FONT_PRIO_FRONT,FONT_PAL_WHITE,jobdaily[(page_index-57)*10+i-1].state,0);
#else
						StockFontBuffer( xx, y+i*25+70, FONT_PRIO_FRONT, desc->color, desc->description, 0);
#endif
					}else{
						//此行只是单纯的说明
						StockFontBuffer( xx, y+i*25+70 + DISPLACEMENT_Y / 2, FONT_PRIO_FRONT, desc->color, desc->description, 0);
					}
				desc++;
			}
		}
	}
	return 0;
}
